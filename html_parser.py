import urllib.request
from bs4 import BeautifulSoup

response = urllib.request.urlopen('https://stackoverflow.com/questions/42611593/how-to-solve-syntaxerror-on-autogenerated-manage-py')
html = response.read()

soup = BeautifulSoup(html, 'html.parser')

#extracting question
question_title = soup.find("a",{"class":"question-hyperlink"}).text
question_body = soup.find("div",{"class":"question"})
qbody = question_body.find_all("p")

qb_list = []
for p in qbody:
	qb_list.append(p.text)

question = question_title
for q in qb_list:
	question = question + "\n" + q
# print(question)

#extracting answer
answers = soup.find_all("div",{"class":"answer"})
answer_list = []
for ans in answers:
	answer_list.append(ans.find("p").text)

author = soup.find_all("div",{"class":"user-details"})
auth_list = []
for auth in author:
	auth_list.append(auth.find("a"))

print(auth_list)
